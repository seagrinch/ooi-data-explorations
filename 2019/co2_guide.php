<h3>Subject / Topics</h3>
<ul>
  <li>Ocean geography:  how and why temperature and other properties differ within and across ocean basins.  </li>
  <li>Water chemistry: dissolved gases, gas solubility</li>
  <li>Air-sea gas exchange:  concept of flux (mass per unit area per unit time). How flux is dependent on wind speed, ocean-atmosphere concentration differences, and gas solubility</li>
  <li>Primary production:  impacts on ocean undersaturation/supersaturation of CO2 and O2</li>
  <li>Carbon cycle:  oceans as a globally significant sink and source of CO2, but overall a net sink under current conditions.  </li>
  <li>Climate change:  oceans take up half of the CO2 humans have emitted to the atmosphere. </li>
  <li>Ocean acidification:  but the uptake of the CO2 comes at a cost-- namely, ocean acidification. </li>
</ul>
<p>It requires taking information in one form, mostly graphs, and conceptualizing about the Oregon continental shelf.</p>


<h3>Grade Level</h3>
<p>Primarily intended for the advanced undergraduate level, but could be used in advanced (11/12) high school courses, introductory undergraduate courses, or at the graduate level depending on how it is scaffolded and what aspects of the module are emphasized. In introductory classes, instructors may want to have students grapple with one or a couple of the concepts and fill in the details for them on the rest. In more advanced classes students should be challenged to recall previously learned information and apply it to this problem. Instructors should be ready to jog their memories about the learned information.</p>


