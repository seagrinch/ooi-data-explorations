<h3>Learning Goals</h3>
<p>After engaging with the Data Exploration a student will be able to:</p>
<ul>
  <li>Identify periods of CO2 equilibrium, disequilibrium, and positive and negative flux of CO2 in the ocean</li>
  <li>Identify patterns in atmospheric and oceanic data</li>
  <li>Draw connections between patterns in atmospheric and oceanic data and patterns in oceanic air-sea CO2 flux</li>
  <li>Evaluate relative importance of causes of variation in air-sea CO2 flux and their variability in time and space</li>
</ul>

<h3>Context for Use</h3>
<p>This exercise could be used in oceanography, biogeochemistry and earth system science courses at all levels, from high school to graduate school, depending on what aspects of the module are emphasized and how the module is scaffolded. For an introductory course, the activity could be used repeatedly throughout the semester as a <em>challenge question</em> that is revisited as new knowledge is gained, resulting in final conclusions by the end of the semester. For upper level courses such as a Physical Oceanography class, it might be a more stand-alone activity late in the semester, although students may still need to be reminded of some of the background knowledge required to piece the story together. For upper-level classes, this module may also be used as a springboard for discussion of geographic variation in carbon uptake and release by the ocean.  Overall, this exploration emphasizes the crucial role of the ocean in Earth's carbon cycle and the double-edged sword that is the ocean?s ability to absorb carbon dioxide.</p>

