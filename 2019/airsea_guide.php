<h3>Summary</h3>
<a href="anoxia.php"><img data-src="holder.js/250x150?text=Dynamic Air-Sea Interactions" alt="Generic placeholder image" width="250" height="150" class="pull-right thumbnail"></a>
<p>This exercise is a case study that, if used in full, takes students through the parts of the learning cycle from exploration to application as applied to an episodic atmospheric event on the New England continental shelf. Pieces of the exercise can be used as appropriate for the instructor goals. Several ideas are given for having students access their previous learning on ocean surface conditions, or give students enough background knowledge to start the exercise (invitation). The tie-in to societal impacts is compelling. Some wave properties data are presented and students are asked to draw some conclusions (exploration). Several types of data are presented as time series data with an interactive widget, and students are asked to speculate on the relationships between the wave properties and different water and atmospheric conditions (application #1). Students are then presented with barometric pressure data and asked to predict precipitation. After their prediction, students can then compare to the actual precipitation data (application #2). </p>

<p>Once students come to a conclusion as to the cause of the event, students are invited to reflect on how this oceanic event affects their life. This case requires students to think of the interaction between atmospheric and sea surface conditions, and the impacts to coastal societies (reflection).</p>


<h3>Learning Goals</h3>
<p>After engaging with this Data Exploration a student will be able to: </p>
<ul>
  <li>Describe patterns in individual data sets and correlations between the different data types presented.</li>
  <li>Interpret the provided data.</li>
  <li>Explain the relationship between oceanic conditions and episodic atmospheric events on the New England continental shelf using evidence and relevant scientific concepts to support their conclusions.</li>
</ul>


<h3>Context for Use</h3>
<p>This exercise could be used for either lower level or upper level students depending on the way it is set up and scaffolded. For an introductory course, the activity could be used repeatedly throughout the semester as individual graphs and questions as new knowledge is gained, resulting in solving the problem by the end of the semester. For upper level courses such as a Physical Oceanography class, it might be a more stand-alone activity late in the semester, although students may still need to be reminded of some of the background knowledge required to piece the story together.</p>

<h3>Teaching Notes</h3>
<p>Students will need to be familiar with concepts from earlier in the semester including: wave properties, surface currents, barometric pressure, low pressure zones, and potential causes of these processes.</p>

<p>At any level, this activity requires synthesis of a variety of concepts that may not have been considered together previously. While the hope is to guide students to the correct answer, the broader learning goal is in making these connections.</p>

