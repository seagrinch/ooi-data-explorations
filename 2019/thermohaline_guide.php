<h3>Subject / Topics</h3>
<ul>
  <li>Ocean thermohaline circulation</li>
  <li>Water properties: temperature and density</li>
  <li>Ocean dynamics: formation of North Atlantic Deep Water</li>
</ul>

<p>This exercise could be used to ask students to apply previously learned concepts, such as wind speed, ocean temperature, and density to explain Deep Water Formation. If these topics have not yet been covered, this example can provide the impetus to learn about them. It requires taking information in one form, mostly graphs, and conceptualizing about the Irminger Sea. In introductory classes, instructors may want to have students grapple with one or a couple of the concepts and fill in the details for them on the rest.</p>
<p>In more advanced classes students should be challenged to recall previously learned information and apply it to this problem. Instructors should be ready to jog their memories about the learned information.</p>


<h3>Grade Levels</h3>
<p>Undergraduate students in Introduction to Oceanography courses (for either marine science majors or non-science majors)</p>
<p>This exercise can also be a case study in upper level Physical Oceanography course, where students apply what they have learned about water properties and deep convection.</p>


<h3>Data Scope</h3>
<p>Our scope in exploring ways to use professionally-collected data in our teaching:</p>
<ul>
  <li>Using professionally-collected data in teaching of concepts</li>
  <li>Visualizing data in a user-friendly and authentic way</li>
  <li>Enabling students to interactively engage with data in their learning to see the patterns as they are learning the why</li>
</ul>


<h3>Quantitative Skills</h3>
<p>Most of the quantitative reasoning in this exercise revolves around reading and interpreting graphs.</p>
<ul>
  <li>Students must articulate the seasonal pattern in the data presented as well as its attenuation with depth.</li>
  <li>With the help of the widget students will examine time series of ocean temperature, ocean density, and wind speed. They should see that there is a strong correlation among all three of these variables.</li>
  <li>Students will be able to connect the formation of dense water to the sinking of this water to drive the thermohaline circulation.</li>
</ul>


