    <h3 class="panel-title"><strong>Application:</strong></h3>
  </div>
  <div class="panel-body">
    <ul>
      <li>Ask students to identify another area in the world where deep water forms. Ask: Are processes similar to what occurs in the Irminger Sea also occurring in that area?  How can you tell?</li>
      <li>Compare the Irminger Sea to the ocean area surrounding Antarctica. Identify similarities between the two areas which could lead to the Antarctic area being a source region for deep water.</li>
      <li>Compare the Irminger Sea to the Gulf of Mexico.  Why are there no areas of deep water formation in the Gulf.</li>
      <li>Have the students speculate why the deepest layer in the Irminger Sea data has no seasonal signal. </li>
      <li>What might happen to the production of deep water if average winter temperatures in this area were to increase as a result of climate change?</li>
      <li>Consider the implications for the global climate if the thermohaline circulation was to considerably slow or even cease.</li>
    </ul>
  </div>
