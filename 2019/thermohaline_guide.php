<h3>Summary</h3>
<img src="images_thermohaline/large/OOI_Arrays_201905509_GI.jpg" alt="OOI_Arrays_201905509_GI" width="350" class="pull-right thumbnail" />
<p>This exercise is a case study that takes students through the entire learning cycle from invitation to reflection as applied to the development of North Atlantic Deep Water in the Irminger Sea. Several types of  data are presented and students are asked to draw some conclusions (exploration). Students are asked to speculate on the relationship between the wind and different water properties and to identify the seasonal cycle in water temperature (concept invention). Students are challenged to apply what they have learned to another situation (application). This case study requires students to think about the interactions among wind, circulation, and water properties, so at the conclusion students are asked to think about what they have learned about this complexity (reflection).</p>


<h3>Learning Goals</h3>
<p>After engaging with this Data Exploration a student will be able to: </p>
<ul>
  <li>Describe patterns in individual graphs and correlations between the different graphs presented.</li>
  <li>Interpret the provided data.</li>
  <li>Explain the relationship between wind speed forcing and ocean water temperature in the near surface.</li>
  <li>Explain the relationship between ocean water temperature and density.</li>
  <li>Identify the seasonal temperature cycle in ocean water and explain how it varies with water depth.</li>
  <li>Describe the formation of NADW and its contribution to the thermohaline circulation.</li>
</ul>


<h3>Context for Use</h3>
<p>This exercise could be used for either lower level or upper level students. For an introductory course, the activity could be used upon completion of the water properties ocean circulation modules to reinforce concepts covered. For upper level courses such as a Physical Oceanography class, it might be a more stand-alone activity used to stimulate questions and review understanding prior to the introduction of the concept of deep convection.</p>


<h3>Teaching Notes</h3>
<p>Students will need to be familiar with concepts from earlier in the semester including: water properties (temperature and density) and basics of the thermohaline circulation.  Advanced students will benefit from a knowledge of latent heat exchange across the air sea interface.</p>
<p>At the lower level, this activity requires synthesis of a variety of concepts (temperature, density, ocean circulation, surface winds) that may not have been considered together previously. At the upper level students can begin to <em>visualize</em> the process of deep convection.</p>
<p>While the hope is to guide students to a more accurate understanding of the concept, the broader learning goal is in making these connections. After the activity is over, discussion could center around how climate change affects the ocean, and the potential for disruption of the global thermohaline circulation.</p>

