  <div class="panel-heading">
    <h3 class="panel-title"><strong>Invitation:</strong> As an introductory activity, you can:</h3>
  </div>
  <div class="panel-body">
    <ul>
      <li>Show a clip of high sea state in the North Atlantic. Invite the students to describe what they observe such as waves and whitecaps.  Ask them to speculate what may be happening at the air-sea interface in terms of winds and the temperatures of both the air and seawater.</li>
      <li>Think back to concepts we've already covered in class that might have something to do with air-sea interactions. Discuss the following prompt with the person next to you: In what ways do you think the atmosphere influences the ocean? What concepts did you discuss that might be in play here?</li>
      <li>Show a T-S diagram with identified water masses to illustrate that water density varies with other water properties.  Remind students they previously learned about water properties such as temperature and salinity.  Ask them which of the properties, salinity or temperature, seems to have the greatest impact on seawater density. What evidence are they basing their answer on?</li>
    </ul>
  </div>
</div>

<div class="panel panel-primary">
  <div class="panel-heading">
    <h3 class="panel-title"><strong>Exploration:</strong></h3>
  </div>
  <div class="panel-body">
    <p><strong>Exploration #1</strong></p>
    <ul>
      <li>Students use the widget to examine the ocean and air temperature plots in relation to the seasonal cycle.  During this exploration, allow students to explore with a partner, make predictions, and try to explain their reasoning.</li>
      <li>Have students turn to the person next to them and describe any pattern(s) they observe in how the ocean surface temperature changes as the seasons change and what might cause this pattern. [Students should be able to identify seasonal changes in the temperature along with the time lag associated with this warming or cooling, as well as the impacts on temperature of periods of sustained high winds.]</li>
      <li>Students make predictions about how other physical factors (sea surface temperature and density) will change in response to changes in wind forcing.
        <ul>
          <li>Have students share with a partner what they think the graph of those physical factors will look like over time, and a rationale for each of their predictions. </li>
          <li>Have students make a quick sketch of their predictions with a few notes about the discussion they had with their partner about their rationales so that they can look back at their ideas later.</li>
        </ul>
      </li>
    </ul>
    <p><strong>Exploration #2</strong></p>
    <ul>
      <li>Students use the widget to examine a stacked set of temperature plots as well as a stacked set of density plots. During this exploration, allow students to explore with a partner, make predictions, and try to explain their reasoning.  During the exercise, have students respond to the following prompts:
        <ul>
          <li>What patterns do you observe? How do the patterns compare to the predictions you made in Exploration #1? In what ways were they similar/different? What additional explanations might explain the results?</li>
          <li>Describe how temperature and density change as you go deeper. Which depths show the greatest amount of seasonal change in temperature? Which depths show the least seasonal change in temperature? </li>
        </ul></li>
      <li>Students use the widget to compare the temperature and density plots at each specific depth to see the relationship between temperature and density. During the exercise, have students discuss the following prompts with a partner or small group:
        <ul>
          <li>What happens to the density as the water gets colder?</li>
          <li>How do these identified changes in the seasonal near-surface temperature and density contribute to the development of thermohaline circulation? </li>
          <li>In what season of the year is this temperature/density relationship strongest? How can you explain that?</li>
          <li>What would happen to the development of the thermohaline circulation if the wintertime near-surface temperatures were to become warmer. </li>
        </ul>
      </li>
      <li>Have students discuss in small groups and then lead a whole group discussion with the following prompt: Is there anything they are wondering about having studied the data that they might not yet be able to explain? What more information do they want or need?</li>
    </ul>
  </div>
</div>

<div class="panel panel-primary">
  <div class="panel-heading">
    <h3 class="panel-title"><strong>Concept Invention:</strong></h3>
  </div>
  <div class="panel-body">
    <ul>
      <li>Lead a whole group discussion about what students have observed so far in the two exploration widgets. Encourage them to share their explanations of the plots and observations, and what questions they have. Help students come to a more accurate scientific explanations of the concepts covered by these widgets.</li>
      <li>Students use the widget to examine the surface wind speed plot and compare it to the near-surface temperature plot to investigate the relationship between wind speed and winter cooling. Have students discuss the following prompts, first with a partner/small group, and then whole group:
        <ul>
          <li>What happens to the near-surface temperature as the wind speed increases in the winter? Why does this occur? What concepts are at play here? </li>
          <li>Where does the lost heat go?</li>
        </ul>
      </li>
      <li>Students use the widget to examine and describe the relationship between wind speed, near-surface water temperature and water density. Have students discuss the following prompts, first with a partner/small group, and then whole group:
        <ul>
          <li>What correlations do you observe between these variables? </li>
          <li>How does what happens at the surface with these water properties produce the cold, high density water found at depth?</li>
        </ul>
      </li>
      <li>Students examine the potential density plots for all depths.  Are they similar? What does this imply about the vertical movement of the water?</li>
      <li>Describe in your own words how the wintertime conditions in the Irminger Sea produce the deep water which forms part of the thermohaline circulation. </li>
      <li>Students use the widget to examine and describe the attenuation with depth of the seasonal cycle of water temperature.  Have students discuss the following prompt, first with a partner/small group, and then whole group:
        <ul>
          <li>Why does the seasonal change of temperature with depth contribute little to the formation of deep water?</li>
        </ul>
      </li>
    </ul>
  </div>
</div>

