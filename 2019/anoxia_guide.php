<h3>Subject / Topics</h3>
<ul>
  <li>Ocean currents: surface currents, upwelling/downwelling, Ekman transport</li>
  <li>Water chemistry: dissolved gases, gas solubility</li>
  <li>Biological rate processes involving oxygen</li>
  <li>Oxygen minimum layers, hypoxic/anoxic areas (dead zones)</li>
  <li>Marine fisheries</li>
</ul>

<p>This exercise could be used to ask students to apply previously learned concepts, such as wind direction and Ekman transport and upwelling, to explain a real scenario. If these topics have not yet been covered, this example can provide the impetus to learn about them.  It requires taking information in one form, mostly graphs, and conceptualizing about the Oregon continental shelf.  In introductory classes, instructors may want to have students grapple with one or a couple of the concepts and fill in the details for them on the rest.</p>
<p>In more advanced classes students should be challenged to recall previously learned information and apply it to this problem.  Instructors should be ready to jog their memories about the learned information.</p>


<h3>Grade Level</h3>
<p>Undergraduate students in Introduction to Oceanography courses (for either marine science majors or non-science majors)</p>
<p>This exercise can be a case study in upper level Physical Oceanography and Chemical Oceanography courses, where students apply what they have learned about dissolved gases, winds, Ekman transport and upwelling and downwelling.</p>


<h3>Data Scope</h3>
<p>Our scope in exploring ways to use professionally-collected data in our teaching:</p>
<ul>
  <li>Using professionally-collected data in teaching of concepts</li>
  <li>Visualizing data in a user-friendly and authentic way</li>
  <li>Enabling students to interactively engage with data in their learning to see the patterns as they are learning the why</li>
</ul>


<h3>Quantitative Skills</h3>
<ul>
  <li>Most of the quantitative reasoning in this exercise revolves around reading and interpreting graphs.
  <ul>
    <li>Water quality is determined by interpreting a graph of dissolved oxygen concentration.  Students are asked to read the graph to determine number of days oxygen concentrations are too low for marine organisms.</li>
    <li>Longshore wind speed is presented in a graph and students must make the connection between the direction on a graph and the direction on a map of the study area.  Understanding this is central to deriving Ekman transport and associated upwelling and downwelling.  </li>
    <li>Students must examine a graph showing the relationship between oxygen saturation and water temperature to understand that the coupled temperature and oxygen data from the Oregon shelf are anomalous.</li>
    <li>They must also articulate patterns in the data presented.</li>
    <li>With the help of the widget students will examine time series of temperature, dissolved oxygen and alongshore wind speed.  They should see that there is a strong correlation between all three of these variables</li>
  </ul></li>
  <li>A vertical section of deep ocean dissolved oxygen concentration provides the final bit of information needed to determine the source of the low dissolved oxygen water.  Students need to examine this color contoured data product to determine where in the water column the low oxygen water resides.</li>
</ul>

<p>For more information on using quantitative skills in higher ed, please see: <a href="http://serc.carleton.edu/quantskills/issues/issues.html">Teaching Quantitative Skills in the Geosciences</a></p>

