<h3>Summary</h3>
<a href="index.php"><img src="../geology/screenshots/mgds_juandefuca_sm.jpg" alt="Juan de Fuca Plate Boundary" width="250" height="150" class="pull-right thumbnail"></a>
<p>This exercise is a case study that, if used in full, takes students through the entire learning cycle from invitation to reflection as applied to a dead zone on the Oregon continental shelf.  Pieces of the exercise can be used as appropriate for the instructor goals.  Several ideas are given for having students access their previous learning on dead zones, or give students enough background knowledge to start the exercise (invitation). The tie-in to life in the ocean is quite compelling. Some data are presented and students are asked to draw some conclusions (exploration).Several types of data are presented, some as time series data with an interactive widget, and students are asked to speculate on the relationships between the wind and different water properties (concept invention).  After coming to a conclusion of the cause of the dead zone students are challenged to apply what they have learned to another situation (application).  This case requires students to think of the interaction between wind, circulation, water properties and biology, so at the conclusion students are asked to think about what they have learned about this complexity (reflection). </p>

<h3>Learning Goals</h3>
<p>After engaging with the Data Exploration a student will be able to: </p>
<ul>
  <li>Describe patterns in individual data sets and correlations between the different data types presented</li>
  <li>Interpret the provided data</li>
  <li>Explain the relationship between wind direction and anoxic events on the Oregon Coast using evidence and relevant scientific concepts to support their conclusions.</li>
</ul>

<h3>Context for Use</h3>
<p>This exercise could be used for either lower level or upper level students depending on the way it is set up and scaffolded. For an introductory course, the activity could be used repeatedly throughout the semester as a ?challenge question? that is returned to as new knowledge is gained, resulting in solving the problem by the end of the semester. For upper level courses such as a Physical Oceanography class, it might be a more stand-alone activity late in the semester, although students may still need to be reminded of some of the background knowledge required to piece the story together.</p>

<h3>Teaching Notes</h3>
<p>Students will need to be familiar with concepts from earlier in the semester including: dead zones; dissolved oxygen, where it comes from and where we expect DO levels to be high and low; Ekman Transport; upwelling/downwelling and potential causes of these processes. </p>
<p>At any level, this activity requires synthesis of a variety of concepts that may not have been considered together previously. While the hope is to guide students to the correct answer, the broader learning goal is in making these connections. The context of dying crabs provides a motivation for students to make this effort. After the activity is over, discussion could center around how climate change affects the ocean, and how life in the ocean could be affected.</p>


