<h2>About the Data Explorations Project</h2>
<p>The National Science Foundation?s Ocean Observatories Initiative (<a href="http://oceanobservatories.org">OOI</a>) is advancing our ability to understand the natural world by collecting large quantities of data to address complex oceanographic processes. This expanded access to data also provides professors in the geosciences with new opportunities to engage undergraduate students in authentic data experiences using real-world data sets to teach geoscience processes. </p>

<p>However, students struggle to work with data based on their limited experience and exposure to different data types and sources. Also, supporting students in engaging with the data can be challenging for professors too, as there is a lack of adequate tools to easily digest and manipulate large data sets for in-class learning experiences. </p>

<h3>What is a Data Exploration?</h3>
<p>We develop interactive data explorations using authentic raw OOI data relevant to Introduction to Oceanography topics to assist with the integration of OOI data into undergraduate oceanography courses.</p>

<p>The data activities are based in:</p>

<ol>
  <li><strong>Learning science research about how people learn.</strong> Currently, data are most often used to demonstrate a concept after it has been taught (i.e., to apply/assess new knowledge), however data can be used to engage a learner in multiple ways. Therefore, multiple versions of each data activity are available that use OOI data with surrounding questions and prompts to enable professors to utilize the data and OOI tools at different phases of a learning cycle based instructional model.</li>
  <li><strong>A framework for supporting novice users interacting with authentic data.</strong> All too often we assume that students can orient themselves to data and that if they can interpret what is on the page they can make the next steps of how that relates to other concepts and/or data sets. However, research and experience demonstrates that novice data users cannot make the same mental jumps in interacting with data as experts can. Therefore, the activities scaffold the students? use of the data visualization through provided questions at the three main steps of interacting with data: orientation, interpretation, and synthesis.</li>
  <li><strong>Best practices of user experience design and usability of data visualizations.</strong> Building off of the current advancements in the user experience design field, the data activities are developed to increase the usability and user positive perceptions of working with data. The data visualization structure is intuitive for novice data users and consistent across all data activities, so that students can move beyond initial orientation to be able to more easily dive deeply into the data with each successive activity used.</li>
</ol>

