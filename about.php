<h2>Exploring Primary Productivity with Data<br>
  <small>About this Project</small>
</h2>

<p>During the spring 2016 workshop we chose to focus on Primary Productivity, as the overarching topic area. Al Trujillo, co-author of Essentials of Oceanography (Trujillo & Thurman, 2017), assisted and participated in the workshop. During the workshop, participants were asked to provide detailed feedback on the activities, including their perceived utility and effectiveness of the activities in the classroom. It is the hope, that each participant will utilize the activities in their class and implement a short student assessment to help us collect some initial information on the students' impression of the OOI activities.</p>

