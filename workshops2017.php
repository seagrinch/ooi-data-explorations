<h3>Workshop Background</h3>
<p>We are inviting you to help us evaluate a collection of new OOI education data examples developed by Rutgers University faculty and education staff. We have developed a series of five short (15-20 minutes) activities that utilized OOI data and/or OOI Education & Public Engagement Tools to augment teaching of Oceanography concepts. Workshop participants will explore ways to effectively teach with data, share effective practices and hopefully expand their understanding of effective pedagogy, and brainstorm ideas for how to integrate OOI data into Introduction to Oceanography courses.</p>

<p>You can review our pilot <a href="productivity/workshop.php">2016 workshop</a> materials which focused on a set of <a href="productivity/">Primary Productivity</a> data explorations.</p>

<p>Our efforts are focused on using Dr. Al Trujillo's <em>Essentials of Oceanography</em> textbook as scope and sequence for the workshop. We will also work with Catherine Halversen from the University of California-Berkeley's Lawrence Hall of Science, to develop effective teaching strategies for using OOI data in teaching.</p>

<h3>What is OOI?</h3>
<p>The <a href="http://oceanobservatories.org">Ocean Observatories Initiative (OOI)</a> has constructed observational and computer infrastructure that provides sustained ocean measurements to study climate variability, ocean circulation, ecosystem dynamics, air-sea exchange, seafloor processes, and plate-scale geodynamics for the coming decades. This ocean observing system is advancing our ability to understand the natural world by accumulating large quantities of data to address complex oceanographic processes.</p>

<p>These workshops are supported by NSF Grant No. OCE-1649637.</p>

