<h3>What is expected of me at the workshop?</h3>
<p>During the workshop, we are asking that each participating professor provide detailed feedback on the developed OOI activities, including their perceived utility and effectiveness for the classroom. In addition, each professor will be invited to teach the OOI activities in their class and implement a short student assessment to help us collect some initial information on the students' impression of the OOI activities.</p>

<h3>Workshop Application</h3>
<p>Each Professor will receive an $800 stipend to support their participation in the workshop, which includes evaluation of OOI activities. Travel, meal and hotel accommodation expenses will also be covered by the National Science Foundation grant, separate from the stipend.  Approximately 10 professors will be selected for each workshop.</p>

