<h3>Summary</h3>
<a href="index.php"><img src="screenshots/water-1555170_960_720_sm.jpg" alt="Ocean Wave" width="250" height="150" class="pull-right thumbnail"></a>
<p>This collection consists of 6 sets of activities, each having 1-2 Data Explorations, to be used during the Exploration or Application phases of the Learning Cycle. The collection was developed to assist with students understanding of various concepts covered in Chapter 5. Water & Seawater (12th Ed) including: salinity variations over time and space, processes that affect surface seawater salinity, changes in pH with depth, relationship between pH and oceanic pCO2 for the carbonate buffering system, variations in salinity with depth over time and space, and the depth of the halocline over time and space. Sample worksheets are provided to assist with students accessing their prior knowledge before beginning the Chapter (Invitation), as well discussion prompts to assist student to think about what understanding about ocean chemistry they have gained through the Chapter (Reflection).</p>

<h3>Learning Goals</h3>
<p>After engaging with the Data Exploration(s) a student will be able to: </p>
<ul>
  <li>Interpret the provided data</li>
  <li>Articulate the concept being presented by answering the Interpretation & Analysis Questions</li>
  <li>Engage with the interactive data widget to support learning</li>
</ul>

<h3>Context for Use</h3>
<p>These Data Explorations are appropriate to use in an Introduction to Oceanography course that is learning about ocean chemistry, specifically variations in salinity, pH, and pCO<sub>2</sub> over time and space. The ability to interpret and use data is integral to oceanography.</p>

<h3>Teaching Notes</h3>
<p>The Data Explorations require access to an internet-ready computer or tablet.</p>
<p>Note, the Data Explorations use authentic raw data. Many of the datasets have been downsampled for simplicity and to ensure that the interactives load quickly in your browser. However, this means that many of the datasets retain their natural variability and some sampling side-effects.  The goal of these activities is for students to analyze authentic data, not smooth averages. have been taken to maintain as much of the data and to keep the variation of the data as true as possible of any data. There has been no resampling, averaging, or selective reduction of the data. Therefore, the amount of data over time varies within the interactive based on the original data and similarly some data is missing.</p>

<p>There are 7 different sets of Data Explorations to choose from within this collection:</p>


