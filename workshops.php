  <h2>OOI Teaching with Data Workshops</h2>
</div>

<p>We engage diverse institutions, including community colleges, Primarily Undergraduate Institutions, Minority-Serving Institutions, Historically Black Colleges & Universities, research laboratories, and larger research universities in intensive hands-on workshops focused on developing effective teaching with data practices using data visualizations tools and current learning science research.</p>

<p>Undergraduate professors participating in the workshop develop strengthened understanding of effective practices in:</p>
<ul>
  <li>Developing a sequence of learning experiences to support student comprehension,</li>
  <li>Setting students up to be successful in understanding data through a tiered framework, and</li>
  <li>Teaching with available authentic real-time OOI data in a user- and professor-friendly manner.</li>
</ul>

