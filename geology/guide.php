<h3>Summary</h3>
<a href="index.php"><img src="screenshots/mgds_juandefuca_sm.jpg" alt="Juan de Fuca Plate Boundary" width="250" height="150" class="pull-right thumbnail"></a>
<p>This collection consists of 4 sets of activities, each having 2-3 Data Exploration activities, to be used during the Exploration or Application phases of the Learning Cycle. The collection was developed to assist with students understanding of various concepts covered in Chapter 2. Plate Tectonics & the Ocean Floor (12th Ed) including: divergent boundary features, transform boundary features, and seamounts. Ideas are provided to assist with students accessing their prior knowledge before beginning an activity (Invitation), developing an understanding of the concept (Concept Invention), and thinking about what understanding about plate tectonics and the ocean floor they have gained through an activity (Reflection).</p>

<h3>Learning Goals</h3>
<p>After engaging with the Data Exploration(s) a student will be able to: </p>
<ul>
  <li>Interpret the provided data</li>  <li>Articulate the concept being presented by answering the Interpretation & Analysis Questions</li>
  <li>Engage with the interactive data widget to support learning</li>
</ul>

<h3>Context for Use</h3>
<p>These Data Explorations are appropriate to use in an Introduction to Oceanography course that is learning about geology, specifically features of divergent and transform plate boundaries and seamounts over time. The ability to interpret and use data is integral to oceanography.</p>

<h3>Teaching Notes</h3>
<p>The Data Exploration activities require access to an internet-ready computer or tablet. Ideally each student or pairs of students would have a computer or tablet to use to engage with the activity.</p>
<p>Note, the Data Explorations use authentic raw data. Many of the datasets have been downsampled for simplicity and to ensure that the interactives load quickly in your browser. However, this means that many of the datasets retain their natural variability and some sampling side-effects.  The goal of these activities is for students to analyze authentic data, not smooth averages. Effort has been taken to maintain as much of the data and to keep the variation of the data as true as possible, but make the activity user-friendly and browser-friendly. There has been no resampling or selective reduction of the data. Therefore, the amount of data over time varies within the interactive based on the original data and similarly, there are time periods when some data are missing.</p>
<p>There are 4 different sets of Data Exploration activities to choose from within this collection:</p>


